// Overlap ledger (AUTOPILOT branching law C-3): every node serving >=2 readings,
// its readings, and the edges that carry it across — the zoom-out made measurable.
// A node "serves" a reading if it grounds an essay in that reading OR sits one
// documented edge from a node that does (reverberation is undirected here).
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(here, '../../..');
const readings = JSON.parse(readFileSync(resolve(here, '../src/data/readings.json'), 'utf8'));

// Overlap is measured across the actual DOORS (Reading I/II/III). The Method and
// the Coda serve/close every reading by definition, so counting them would make
// overlap trivially universal — they are excluded from the reading-membership.
const groups = readings.readings.map((r) => ({ key: r.slug, label: `${r.num} · ${r.title}`, essays: r.essays }));

// essay -> grounding_nodes
function walk(dir) { const out = []; for (const e of readdirSync(dir, { withFileTypes: true })) { const p = resolve(dir, e.name); if (e.isDirectory()) out.push(...walk(p)); else if (e.name.endsWith('.md')) out.push(p); } return out; }
const grounding = {};
for (const f of walk(resolve(ROOT, 'essays'))) {
  const md = readFileSync(f, 'utf8');
  const m = md.match(/grounding_nodes:\s*\n((?:\s*-\s*.+\n?)+)/);
  const id = (md.match(/^title:/m) ? f.split('/').pop().replace(/\.md$/, '') : null);
  if (!m || !id) continue;
  grounding[id] = m[1].split('\n').map((l) => l.replace(/^\s*-\s*/, '').trim()).filter(Boolean);
}

// node edges (undirected adjacency + degree)
const adj = new Map();
const degree = new Map();
const addEdge = (a, b) => {
  if (!a || !b) return;
  if (!adj.has(a)) adj.set(a, new Set()); adj.get(a).add(b);
  if (!adj.has(b)) adj.set(b, new Set()); adj.get(b).add(a);
  degree.set(a, (degree.get(a) || 0) + 1);
  degree.set(b, (degree.get(b) || 0) + 1);
};
const nodeEdges = new Map();
for (const f of walk(resolve(ROOT, 'data/nodes'))) {
  const md = readFileSync(f, 'utf8');
  const id = (md.match(/^id:\s*(.+)$/m) || [])[1]?.trim();
  if (!id) continue;
  const edges = [...md.matchAll(/\{to:\s*([a-z0-9-]+),\s*type:\s*([a-z-]+)/g)].map((e) => ({ to: e[1], type: e[2] }));
  nodeEdges.set(id, edges);
  for (const e of edges) addEdge(id, e.to);
}

// seed reading membership by grounding, then one-hop propagate along edges
const serves = new Map(); // node -> Set(groupKey)
const seed = (node, key) => { if (!serves.has(node)) serves.set(node, new Set()); serves.get(node).add(key); };
for (const g of groups) for (const es of g.essays) for (const n of grounding[es] || []) seed(n, g.key);
// one hop
const base = new Map([...serves].map(([k, v]) => [k, new Set(v)]));
for (const [node, keys] of base) for (const nb of adj.get(node) || []) for (const k of keys) seed(nb, k);

// collect >=2 (real nodes only — guard against frontmatter/parse artifacts)
const realNodes = new Set(nodeEdges.keys());
const rows = [...serves.entries()]
  .filter(([node, s]) => realNodes.has(node) && s.size >= 2)
  .map(([node, s]) => ({ node, readings: [...s], deg: degree.get(node) || 0 }))
  .sort((a, b) => b.readings.length - a.readings.length || b.deg - a.deg || a.node.localeCompare(b.node));

const labelOf = Object.fromEntries(groups.map((g) => [g.key, g.label]));
const crossEdges = (node, keys) => (nodeEdges.get(node) || [])
  .filter((e) => { const s = serves.get(e.to); return s && [...s].some((k) => !keys.includes(k)); })
  .map((e) => `${e.type}→${e.to}`).slice(0, 4).join(', ');

let out = `# OVERLAP.md — the overlap ledger (the zoom-out, measured)
*AUTOPILOT branching law C-3. Every node serving ≥2 readings, its readings, and the edges that carry it across. A node serves a reading if it grounds an essay in that reading or sits one documented edge from a node that does. Refresh on every cluster pass. Generated by scripts/gen-overlap.mjs.*

**${rows.length} nodes serve ≥2 readings.** Hub nodes serving multiple readings are EXPECTED — overlap is the evidence of reverberation, never a defect to "fix."

| node | readings | degree | carrying edges (toward another reading) |
|---|---|---|---|
`;
for (const r of rows) {
  out += `| \`${r.node}\` | ${r.readings.map((k) => labelOf[k]).join(' · ')} | ${r.deg} | ${crossEdges(r.node, r.readings) || '—'} |\n`;
}
out += `\n## Top hubs by reading-count, then degree\n`;
for (const r of rows.slice(0, 10)) out += `- \`${r.node}\` — ${r.readings.length} readings, degree ${r.deg}\n`;

out += `\n## Maximum-degree nodes (the zoom-out receipts)\n*Raw edge degree across the whole graph. As Reading II's occult rails branch (Music §C, Film §D, Religion §E — all documented to route through Crowley), \`crowley-aleister\` climbs this table; today the trunk hub still leads on raw restatement-degree.*\n`;
const byDeg = [...degree.entries()].filter(([n]) => realNodes.has(n)).sort((a, b) => b[1] - a[1]).slice(0, 12);
for (const [n, d] of byDeg) out += `- \`${n}\` — degree ${d}\n`;

writeFileSync(resolve(ROOT, 'research/OVERLAP.md'), out);
console.log(`[gen-overlap] ${rows.length} nodes serve >=2 readings; top: ${rows.slice(0, 3).map((r) => r.node).join(', ')}`);
