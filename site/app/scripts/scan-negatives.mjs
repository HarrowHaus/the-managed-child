// L3 (Negative-Claim Law) scanner — MASTER-SPEC 2.6, and Recovery-Pass step 1
// ("inventory the prosecutions"). Flags sentences carrying a negative-universal
// pattern that lack a scoping marker (a claim about THIS PROJECT's claim, or a
// search universe). Report-mode: it writes research/negatives-audit.md for
// Recovery Pass 1 to adjudicate (BOUND with a search universe, RESTORE, or
// confirm-as-documented-fact). It flips to a hard build lint once Recovery Pass 1
// has cleared the corpus. A documented non-meeting ("X and Y never met") is a
// FACT, not an illegal universal — Recovery Pass decides; this only surfaces.
import { readFileSync, writeFileSync, readdirSync, statSync } from 'node:fs';
import { resolve, dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(here, '../../..');

// Bare negative-universal patterns (L3's illegal phrases + close kin).
const NEG = /\b(no relay|cannot be found|never touched|none read|built nothing|no line of contact|no evidence (?:that|of)|no thread|never met|no documented (?:connection|tie|contact)|no trace)\b/i;
// Scoping markers that make a negative LEGAL (bounded / claim-scoped / a search).
// A negative is LEGAL when it is scoped to the project's claim, bounded to a
// search, or scoped to "documented" (a bounded evidentiary negative, not a
// universal). "no documented connection/tie/contact" is the correct bounded form.
const SCOPED = /\b(claimed|in the (?:record|sources)(?: (?:searched|checked))?|not established|is established|searched|documented absence|not asserted|no source stands|no such claim|no documented|does not (?:support|show)|the record does not)\b/i;

function walk(dir) {
  const out = [];
  for (const n of readdirSync(dir)) {
    const p = join(dir, n);
    if (statSync(p).isDirectory()) out.push(...walk(p));
    else if (n.endsWith('.md')) out.push(p);
  }
  return out;
}

const files = [...walk(resolve(ROOT, 'essays')), ...walk(resolve(ROOT, 'data/nodes'))]
  .filter((f) => !/_parked-decodes/.test(f));

const hits = [];
for (const f of files) {
  const rel = f.replace(ROOT + '/', '');
  readFileSync(f, 'utf8').split('\n').forEach((line, i) => {
    if (NEG.test(line) && !SCOPED.test(line)) {
      const m = line.match(NEG);
      hits.push({ file: rel, line: i + 1, phrase: m[0], text: line.trim().slice(0, 240) });
    }
  });
}

let md = `# negatives-audit.md — L3 candidate inventory (Recovery-Pass step 1)
*Generated by scripts/scan-negatives.mjs. Each row is a sentence carrying a negative-universal pattern WITHOUT a scoping marker. Recovery Pass 1 adjudicates each: **BOUND** (rewrite to "not established in the sources checked" + search universe), **RESTORE** (the evidence supports more — un-hedge), or **FACT** (a specific documented non-relay/non-meeting, kept, optionally re-phrased to the bounded form). Not a build gate yet — flips to hard-fail once this list is cleared.*

**${hits.length} candidate(s).**

| file | line | phrase | sentence |
|---|---|---|---|
`;
for (const h of hits) md += `| \`${h.file}\` | ${h.line} | ${h.phrase} | ${h.text.replace(/\|/g, '\\|')} |\n`;
writeFileSync(resolve(ROOT, 'research/negatives-audit.md'), md);
console.log(`[scan-negatives] ${hits.length} L3 candidate(s) → research/negatives-audit.md`);
